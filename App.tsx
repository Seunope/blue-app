import React, {useEffect} from 'react';
import RootNavigation from './src/route/root';
import RNBootSplash from 'react-native-bootsplash';
import BlueAppFonts from './src/config/utils/fonts';
import BlueAppColors from './src/config/utils/colors';
import BlueAppCustom from './src/config/utils/custom';
import {NativeBaseProvider, extendTheme} from 'native-base';
// import {QueryClientProvider, QueryClient} from 'react-query';

const theme = extendTheme({
  colors: BlueAppColors,
  components: BlueAppCustom,
  fonts: BlueAppFonts.fontName,
  fontConfig: BlueAppFonts.primaryFont,
});

const App = () => {
  useEffect(() => {
    initBootSplash();
  }, []);

  const initBootSplash = async () => {
    try {
      await RNBootSplash.hide({fade: true});
    } catch (e) {
      console.log(e);
    }
  };
  const config = {
    dependencies: {
      'linear-gradient': require('react-native-linear-gradient').default,
    },
  };

  return (
    <NativeBaseProvider theme={theme} config={config}>
      <RootNavigation />
    </NativeBaseProvider>
  );
};
export default App;
